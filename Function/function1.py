# A function to add two numbers together
def addition(a,b):
    r=a+b
    print(r)
    
addition(6,4)


#A function to subtract one number from another
def subtraction(m,n):
    x=m-n
    print(x)
    
subtraction(70,8)

# A function to multiply two numbers
def multiply(a,b):
    r=a*b
    print(r)
    
multiply(6,4)


#A function to divide one number by another
def division(m,n):
    x=m/n
    print(x)
    
division(70,10)
